package Class_Interface_Part_.Demo01;


public class Hero {
    private String name; // Hero's name
    private int age; // Hero's age
    private Weapon weapon; // Hero's weapon

    public Hero() {
    }

    public Hero(String name, int age, Weapon weapon) {
        this.name = name;
        this.age = age;
        this.weapon = weapon;
    }

    public void attack() {
        System.out.println("The age is: " + age + " whose: " + name + " use " + weapon.getCode() + " attack the enemy! ");
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public Weapon getWeapon() {
        return weapon;
    }

    public void setWeapon(Weapon weapon) {
        this.weapon = weapon;
    }
}
